package ru.arkady.journal.security;

import lombok.AllArgsConstructor;
import lombok.Data;
import org.springframework.security.core.GrantedAuthority;
import org.springframework.security.core.authority.SimpleGrantedAuthority;
import org.springframework.security.core.userdetails.User;
import org.springframework.security.core.userdetails.UserDetails;
import ru.arkady.journal.entities.enums.Status;
import ru.arkady.journal.entities.Teacher;

import java.util.Collection;
import java.util.List;

@Data
@AllArgsConstructor
public class SecurityUser implements UserDetails {

    private final String username;
    private final String password;
    private final List<SimpleGrantedAuthority> authorityList;
    private final boolean isActive;

    @Override
    public Collection<? extends GrantedAuthority> getAuthorities() {
        return authorityList;
    }

    @Override
    public String getPassword() {
        return password;
    }

    @Override
    public String getUsername() {
        return username;
    }

    @Override
    public boolean isAccountNonExpired() {
        return isActive;
    }

    @Override
    public boolean isAccountNonLocked() {
        return isActive;
    }

    @Override
    public boolean isCredentialsNonExpired() {
        return isActive;
    }

    @Override
    public boolean isEnabled() {
        return isActive;
    }

    public static UserDetails fromUser(Teacher teacher){
        return new User(teacher.getPhone(),
                teacher.getPassword(),
                teacher.getStatus().equals(Status.ACTIVE),
                teacher.getStatus().equals(Status.ACTIVE),
                teacher.getStatus().equals(Status.ACTIVE),
                teacher.getStatus().equals(Status.ACTIVE),
                teacher.getRole().getAuthorities()
        );
    }
}
